package variable;

public class Var2 {
    public static void main(String[] args) {
        // 변수 : 값을 꺼내서 읽을 수 있는 저장소
        int a; // 변수 선언
        // 자바에서 =은 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻이다.
        // 선언한 변수에 처음으로 값을 대입해서 저장하는 것을 변수 초기화
        a = 20; // 10 -> 20으로 변경

        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

    }
}
