package variable;

public class Var3 {
    public static void main(String[] args) {
        // 프로그램은 한 줄씩 순서대로 실행된다.
        int a; // 변수 선언
        a = 10; // 변수 초기화 a(10)
        System.out.println(a);
        a = 50; // 변수 값 변경 a(10 -> 50)
        System.out.println(a);
        // 변수의 값을 변경하면 변쉥 들어있던 기존 값은 삭제된다.
    }
}
